//Las constantes usan la keyword const y no se pueden redeclarar o reasignar
//Se declararán e inicializarán tres constantes: un número, un string y un array
//NOTA: Por buenas prácticas, declararé en mayúsculas las nombres de las constantes :)
const NUMBER = 32;
const STRING = "MASHA";
const ARRAY = [1,2,3,4,5];

//Se muestran las constantes en consola
console.log("Número: ", NUMBER);
console.log("String: ", STRING);
console.log("Array: ", ARRAY);